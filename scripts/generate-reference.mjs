/**
 * Generate docs/generated-reference.md from the Commander tree + COMMAND_METADATA.
 *
 * Usage:
 *   node scripts/generate-reference.mjs          # preview to stdout
 *   node scripts/generate-reference.mjs --write   # write to docs/reference.md
 *   node scripts/generate-reference.mjs --check   # compare with docs/reference.md, exit 1 on drift
 *
 * Requires a prior build (`bun run build`).
 */

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = dirname(scriptDir);

// ── Guard: dist/ must exist ──

const distPath = join(repoRoot, "dist", "program.js");
if (!existsSync(distPath)) {
  console.error(
    "Built CLI not found. Run `bun run build` first, or use `bun run dev -- ...` from source.",
  );
  process.exit(1);
}

// ── Imports from built artifacts ──

const { createRootProgram } = await import(join(repoRoot, "dist", "program.js"));
const {
  COMMAND_METADATA,
  CAPABILITIES_COMMAND_ORDER,
  GLOBAL_FLAG_METADATA,
  CAPABILITIES_SCHEMAS,
} = await import(join(repoRoot, "dist", "utils", "command-metadata.js"));

// ── Build Commander tree for flag descriptions ──

const program = createRootProgram("0.0.0");

/** Build a name→Command map including subcommands (space-separated paths). */
function buildCommandMap(cmd, prefix = "") {
  const map = new Map();
  for (const sub of cmd.commands) {
    const path = prefix ? `${prefix} ${sub.name()}` : sub.name();
    map.set(path, sub);
    // Recurse for subcommands (e.g., "stats global", "withdraw quote")
    buildCommandMap(sub, path).forEach((v, k) => map.set(k, v));
  }
  return map;
}

const commandMap = buildCommandMap(program);

// ── Helpers ──

function escapeMarkdownPipe(str) {
  return str.replace(/\|/g, "\\|");
}

function getCommandOptions(cmd, capabilityFlags) {
  // Build a set of flag names from capabilities metadata to include
  // hidden options that are documented in the command's capabilities.
  const capFlagNames = new Set();
  if (capabilityFlags) {
    for (const f of capabilityFlags) {
      const match = f.match(/^(--[\w-]+)/);
      if (match) capFlagNames.add(match[1]);
    }
  }

  const opts = [];
  for (const opt of cmd.options) {
    if (opt.long === "--help" || opt.long === "--version") continue;
    // Include visible options, plus hidden ones documented in capabilities
    if (opt.hidden && !(opt.long && capFlagNames.has(opt.long))) continue;
    const flags = opt.flags;
    const desc = opt.description || "";
    opts.push({ flags, description: desc });
  }
  return opts;
}

// ── Generate markdown ──

const lines = [];

lines.push("<!-- AUTO-GENERATED by scripts/generate-reference.mjs - DO NOT EDIT -->");
lines.push("");
lines.push("# CLI Reference");
lines.push("");
lines.push("Detailed command reference for the Privacy Pools CLI. For a quick overview, see the [README](../README.md). For agent integration, see [AGENTS.md](../AGENTS.md).");
lines.push("");
lines.push("## Commands");

for (const path of CAPABILITIES_COMMAND_ORDER) {
  const metadata = COMMAND_METADATA[path];
  if (!metadata) continue;

  const cmd = commandMap.get(path);
  if (!cmd) continue;

  lines.push("");
  lines.push(`### \`${path}\``);
  lines.push("");
  lines.push(metadata.description);

  // Usage line with positional arguments from Commander
  const cmdArgs = cmd.registeredArguments || [];
  if (cmdArgs.length > 0) {
    const argSyntax = cmdArgs
      .map((a) => (a.required ? `<${a.name()}>` : `[${a.name()}]`))
      .join(" ");
    lines.push("");
    lines.push(`**Usage:** \`privacy-pools ${path} ${argSyntax} [options]\``);
  }

  // Overview prose from metadata
  const overview = metadata.help?.overview ?? [];
  if (overview.length > 0) {
    // Filter out lines that are indented shell setup commands (duplicates of examples)
    const filtered = overview.filter(
      (line) => !(line.startsWith("  ") && line.includes("privacy-pools"))
    );
    // Also drop trailing header lines that introduce removed content (e.g., "Setup (add to...):")
    while (
      filtered.length > 0 &&
      filtered[filtered.length - 1].trim().endsWith(":")
    ) {
      filtered.pop();
    }
    // Check if any non-empty lines are indented (terminal-formatted)
    const hasContent = filtered.some((line) => line.trim().length > 0);
    if (hasContent) {
      const hasIndented = filtered.some((line) => line.startsWith("  ") && line.trim().length > 0);
      lines.push("");
      if (hasIndented) {
        // Render as blockquote to preserve terminal formatting
        for (const line of filtered.filter((l) => l.trim().length > 0)) {
          lines.push(`> ${line}`);
        }
      } else {
        // Group lines into paragraphs separated by empty lines
        const paragraphs = [];
        let current = [];
        for (const line of filtered) {
          if (line.trim().length === 0) {
            if (current.length > 0) {
              paragraphs.push(current.join(" "));
              current = [];
            }
          } else {
            current.push(line);
          }
        }
        if (current.length > 0) {
          paragraphs.push(current.join(" "));
        }
        lines.push(paragraphs.join("\n\n"));
      }
    }
  }

  // Examples from metadata
  const examples = metadata.help?.examples ?? [];
  if (examples.length > 0) {
    lines.push("");
    lines.push("```bash");
    for (const ex of examples) {
      lines.push(ex);
    }
    lines.push("```");
  }

  // Flag table from Commander (for human descriptions)
  const options = getCommandOptions(cmd, metadata.capabilities?.flags);
  if (options.length > 0) {
    lines.push("");
    lines.push("| Flag | Description |");
    lines.push("|------|-------------|");
    for (const opt of options) {
      lines.push(`| \`${escapeMarkdownPipe(opt.flags)}\` | ${escapeMarkdownPipe(opt.description)} |`);
    }
  }

  // Safety notes from metadata
  const safetyNotes = metadata.help?.safetyNotes ?? [];
  if (safetyNotes.length > 0) {
    lines.push("");
    for (const note of safetyNotes) {
      lines.push(`**Safety:** ${note}`);
    }
  }

  // JSON output shape hint
  const jsonFields = metadata.help?.jsonFields;
  if (jsonFields) {
    lines.push("");
    lines.push(`**JSON output:** \`${jsonFields}\``);
  }
}

// ── Global Flags ──

lines.push("");
lines.push("## Global Flags");
lines.push("");
lines.push("| Flag | Description |");
lines.push("|------|-------------|");
for (const { flag, description } of GLOBAL_FLAG_METADATA) {
  lines.push(`| \`${escapeMarkdownPipe(flag)}\` | ${escapeMarkdownPipe(description)} |`);
}

// ── Exit Codes ──

const exitCodes = CAPABILITIES_SCHEMAS?.errorCategories?.exitCodes;
if (exitCodes) {
  lines.push("");
  lines.push("## Exit Codes");
  lines.push("");
  lines.push("| Code | Category | Meaning |");
  lines.push("|------|----------|---------|");
  lines.push("| 0 | | Success |");

  // Sort by exit code value
  const entries = Object.entries(exitCodes).sort((a, b) => a[1] - b[1]);
  for (const [category, code] of entries) {
    const meaning = {
      INPUT: "Invalid input or validation failure",
      RPC: "RPC / network error",
      ASP: "ASP service error",
      RELAYER: "Relayer service error",
      PROOF: "ZK proof generation error",
      CONTRACT: "On-chain contract revert",
      UNKNOWN: "General error",
    }[category] || category;
    lines.push(`| ${code} | ${category} | ${meaning} |`);
  }
}

// ── Static sections ──

const staticPath = join(repoRoot, "docs", "reference-static-sections.md");
if (existsSync(staticPath)) {
  lines.push("");
  lines.push(readFileSync(staticPath, "utf8").trimEnd());
}

lines.push("");

const output = lines.join("\n");

// ── Mode dispatch ──

const args = process.argv.slice(2);

if (args.includes("--write")) {
  const outPath = join(repoRoot, "docs", "reference.md");
  writeFileSync(outPath, output);
  console.log(`Wrote ${outPath}`);
} else if (args.includes("--check")) {
  const refPath = join(repoRoot, "docs", "reference.md");
  if (!existsSync(refPath)) {
    console.error(`${refPath} not found.`);
    process.exit(1);
  }
  const current = readFileSync(refPath, "utf8");
  if (current !== output) {
    console.error("docs/reference.md is out of date. Run `bun run docs:generate` to regenerate.");
    process.exit(1);
  }
  console.log("docs/reference.md is up to date.");
} else {
  process.stdout.write(output);
}
