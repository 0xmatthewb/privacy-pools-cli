import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = dirname(scriptDir);

const distModulePath = join(
  repoRoot,
  "dist",
  "utils",
  "command-discovery-metadata.js",
);

const sourceModulePath = join(
  repoRoot,
  "src",
  "utils",
  "command-discovery-static.ts",
);

const { COMMAND_PATHS, buildCapabilitiesPayload } = await import(
  pathToFileURL(distModulePath).href
);

const capabilitiesPayload = buildCapabilitiesPayload();

const aliasEntries = COMMAND_PATHS.flatMap((path) =>
  (capabilitiesPayload.commandDetails[path]?.aliases ?? []).map((alias) => [
    alias,
    path,
  ]),
);

const aliasMap = Object.fromEntries(aliasEntries);

const fileContents = `/* AUTO-GENERATED by scripts/generate-command-discovery-static.mjs - DO NOT EDIT */

import type { CapabilitiesPayload } from "../types.js";

export const STATIC_COMMAND_PATHS = ${JSON.stringify(COMMAND_PATHS, null, 2)} as const;

export type StaticCommandPath = (typeof STATIC_COMMAND_PATHS)[number];

const STATIC_COMMAND_PATH_SET = new Set<string>(STATIC_COMMAND_PATHS);

const STATIC_COMMAND_ALIAS_MAP: Record<string, StaticCommandPath> = ${JSON.stringify(aliasMap, null, 2)};

export const STATIC_CAPABILITIES_PAYLOAD: CapabilitiesPayload = ${JSON.stringify(capabilitiesPayload, null, 2)};

export const STATIC_GLOBAL_FLAG_METADATA = STATIC_CAPABILITIES_PAYLOAD.globalFlags;

export function resolveStaticCommandPath(
  query: string | string[],
): StaticCommandPath | null {
  const normalized = Array.isArray(query)
    ? query.join(" ").trim().replace(/\\s+/g, " ")
    : query.trim().replace(/\\s+/g, " ");

  if (!normalized) {
    return null;
  }

  if (STATIC_COMMAND_PATH_SET.has(normalized)) {
    return normalized as StaticCommandPath;
  }

  return STATIC_COMMAND_ALIAS_MAP[normalized] ?? null;
}

export function listStaticCommandPaths(): StaticCommandPath[] {
  return [...STATIC_COMMAND_PATHS];
}
`;

mkdirSync(dirname(sourceModulePath), { recursive: true });
writeFileSync(sourceModulePath, fileContents, "utf8");
